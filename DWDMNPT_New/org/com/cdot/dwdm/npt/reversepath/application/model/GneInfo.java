package application.model;

public class GneInfo {

	int gneFlag;
	String gneIp;
	int vlan;
	String subnetMask;
	String gateway;
	String ipv6;
	
	public int getGneFlag() {
		return gneFlag;
	}
	public void setGneFlag(int gneFlag) {
		this.gneFlag = gneFlag;
	}
	public String getGneIp() {
		return gneIp;
	}
	public void setGneIp(String gneIp) {
		this.gneIp = gneIp;
	}
	public int getVlan() {
		return vlan;
	}
	public void setVlan(int vlan) {
		this.vlan = vlan;
	}
	public String getSubnetMask() {
		return subnetMask;
	}
	public void setSubnetMask(String subnetMask) {
		this.subnetMask = subnetMask;
	}
	public String getGateway() {
		return gateway;
	}
	public void setGateway(String gateway) {
		this.gateway = gateway;
	}
	public String getIpv6() {
		return ipv6;
	}
	public void setIpv6(String ipv6) {
		this.ipv6 = ipv6;
	}

	
}

















